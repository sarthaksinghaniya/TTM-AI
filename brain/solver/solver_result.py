"""SolverResult model definitions representing output of any solver."""

from enum import StrEnum
from pydantic import BaseModel, ConfigDict, Field, field_validator

from brain.models.exceptions import ValidationError
from brain.models.scheduled_assignment import ScheduledAssignment


class SolverStatus(StrEnum):
    """Execution state of the solver after processing."""

    OPTIMAL = "OPTIMAL"
    FEASIBLE = "FEASIBLE"
    INFEASIBLE = "INFEASIBLE"
    TIMEOUT = "TIMEOUT"
    ABORTED = "ABORTED"
    UNKNOWN = "UNKNOWN"


class SolverResult(BaseModel):
    """Unified container for outputs generated by any timetabling solver.

    Attributes:
        status: Solution feasibility state.
        scheduled_assignments: Tuple of ScheduledAssignment instances.
        statistics: Telemetry key-value statistics.
        diagnostics: Telemetry key-value logs or diagnostics.
        runtime: Solve execution duration in seconds.
        objective_value: Evaluated objective score if soft constraints exist.
    """

    model_config = ConfigDict(frozen=True, extra="forbid")

    status: SolverStatus
    scheduled_assignments: tuple[ScheduledAssignment, ...] = ()
    statistics: dict[str, str | int | float | bool] = Field(default_factory=dict)
    diagnostics: dict[str, str | int | float | bool] = Field(default_factory=dict)
    runtime: float
    objective_value: float | int | None = None

    @field_validator("runtime")
    @classmethod
    def validate_non_negative_runtime(cls, v: float) -> float:
        """Validate that the solver execution runtime is non-negative.

        Args:
            v: Input runtime value.

        Returns:
            The validated float.

        Raises:
            ValidationError: If runtime is negative.
        """
        if v < 0.0:
            raise ValidationError("runtime", "Runtime cannot be negative")
        return v
